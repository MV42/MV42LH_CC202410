#include "libft.h"

// Deletes a single node from a linked list.
// Applies the 'del' function to the node's content then frees the node.
// Does nothing if 'lst' or 'del' is NULL.
void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (!lst || !del)
		return ;
	del(lst->content);
	free(lst);
}
